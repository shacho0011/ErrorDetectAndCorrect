package model;

/**
 *
 * @author Shadhin
 */
public class Master {
    private int id;
    private String ssn;
    private String name;
    private String phone;
    private String street;
    private String city;

    public Master(int id, String ssn, String name, String phonr, String street, String city) {
        this.id = id;
        this.ssn = ssn;
        this.name = name;
        this.phone = phonr;
        this.street = street;
        this.city = city;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSsn() {
        return ssn;
    }

    public void setSsn(String ssn) {
        this.ssn = ssn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }
    
    
}
